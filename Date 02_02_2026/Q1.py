# PEP-8 and  clean code

'''PEP-8 is stand for Python Enhancement Proposals
    it is the  official 8 guide for python 
    it tells you how to write python code so that it is
    1. easy to read 
    2. consistent
    3. professional
    4. easy for others (and future you) to understand'''


# 1. Indentation
''' use 4 spaces per indentation level
never mix tabs and spaces'''

# 2. Maximum Line Length
''' limit lines to 79 charaters
 long lines should be broken into multiple lines'''

# 3. naming conventions

''' snake_case
    PascalCase
    UPPER_CASE'''

# 4. Spacing Rules

''' x = a + b    correct'''

# 5. Blank Lines
''' 2 blank lines betweeen top-level classes/functions
    1 blank line between methods inside a class this improves readability.'''

# 6. Imports
'''  imports should be at the top of the file
     one import per line'''

# 7. Comments
''' Comments should be clear and meaningful
    start with # and a space'''

